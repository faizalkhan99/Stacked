public enum SwingDirection
{
    // Cardinal Directions
    Positive_X, // Right
    Negative_X, // Left
    Positive_Z, // Forward
    Negative_Z, // Back
    
    // Diagonal Directions
    Positive_X_Positive_Z, // Forward-Right
    Positive_X_Negative_Z, // Back-Right
    Negative_X_Positive_Z, // Forward-Left
    Negative_X_Negative_Z  // Back-Left
}